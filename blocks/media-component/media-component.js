export default function decorate(b) {
  b.classList.add('media-component');
  const bg = b.querySelector('.media-background');
  if (bg) bg.classList.add('media-background');
  const wrapper = b.querySelector('.media-wrapper');
  if (wrapper) wrapper.classList.add('media-wrapper');
  const header = b.querySelector('.media-header');
  if (header) header.classList.add('media-header');
  const heading = b.querySelector('.media-heading');
  if (heading) heading.classList.add('media-heading');
  const title = b.querySelector('.media-title');
  if (title) title.classList.add('media-title');
  const semiBold = b.querySelector('.media-semi-bold');
  if (semiBold) semiBold.classList.add('media-semi-bold');
  const poster = b.querySelector('.media-video-poster');
  if (poster) poster.classList.add('media-video-poster');
  const playBtn = b.querySelector('.media-video-poster__play-button');
  if (playBtn) playBtn.classList.add('media-video-poster__play-button');
  const playIcon = b.querySelector('.media-video-poster__play-button__icon');
  if (playIcon) playIcon.classList.add('media-video-poster__play-button__icon');
  const playText = b.querySelector('.media-video-poster__play-button__text');
  if (playText) playText.classList.add('media-video-poster__play-button__text');
  const posterVideo = b.querySelector('.media-video-poster__video');
  if (posterVideo) posterVideo.classList.add('media-video-poster__video');
  const videoContainer = b.querySelector('.media-video-container');
  if (videoContainer) videoContainer.classList.add('media-video-container');
  if (videoContainer && videoContainer.classList.contains('media-video-hide')) {
    videoContainer.classList.add('media-video-hide');
  }
  const controls = b.querySelector('.media-video-container__controls');
  if (controls) controls.classList.add('media-video-container__controls');
  const timer = b.querySelector('.media-video-container__controls__timer');
  if (timer) timer.classList.add('media-video-container__controls__timer');
  const progressArea = b.querySelector('.media-video-container__controls__timer__progress-area');
  if (progressArea) progressArea.classList.add('media-video-container__controls__timer__progress-area');
  const progressBar = b.querySelector('.media-video-container__controls__timer__progress-area__progress-bar');
  if (progressBar) progressBar.classList.add('media-video-container__controls__timer__progress-area__progress-bar');
  const pointer = b.querySelector('.media-video-container__controls__timer__progress-area__pointer');
  if (pointer) pointer.classList.add('media-video-container__controls__timer__progress-area__pointer');
  const progressPending = b.querySelector('.media-video-container__controls__timer__progress-area__progress-pending');
  if (progressPending) progressPending.classList.add('media-video-container__controls__timer__progress-area__progress-pending');
  const currentTime = b.querySelector('.media-video-container__controls__timer__current-time');
  if (currentTime) currentTime.classList.add('media-video-container__controls__timer__current-time');
  const duration = b.querySelector('.media-video-container__controls__timer__duration');
  if (duration) duration.classList.add('media-video-container__controls__timer__duration');
  const buttons = b.querySelector('.media-video-container__controls__buttons');
  if (buttons) buttons.classList.add('media-video-container__controls__buttons');
  const playBtn2 = b.querySelector('.media-video-container__controls__buttons__play-button');
  if (playBtn2) playBtn2.classList.add('media-video-container__controls__buttons__play-button', 'media-video-container__controls__buttons--button');
  const playIcon2 = b.querySelector('.media-video-container__controls__buttons__icon.media-qd-icon--play');
  if (playIcon2) playIcon2.classList.add('media-video-container__controls__buttons__icon', 'media-qd-icon', 'media-qd-icon--play');
  const muteBtn = b.querySelector('.media-video-container__controls__buttons__mute-button');
  if (muteBtn) muteBtn.classList.add('media-video-container__controls__buttons__mute-button', 'media-video-container__controls__buttons--button');
  const muteIcon = b.querySelector('.media-video-container__controls__buttons__icon.media-qd-icon--volume');
  if (muteIcon) muteIcon.classList.add('media-video-container__controls__buttons__icon', 'media-qd-icon', 'media-qd-icon--volume');
  const fullscreenBtn = b.querySelector('.media-video-container__controls__buttons__fullscreen-button');
  if (fullscreenBtn) fullscreenBtn.classList.add('media-video-container__controls__buttons__fullscreen-button', 'media-video-container__controls__buttons--button');
  const fullscreenIcon = b.querySelector('.media-video-container__controls__buttons__icon.media-qd-icon--fullscreen');
  if (fullscreenIcon) fullscreenIcon.classList.add('media-video-container__controls__buttons__icon', 'media-qd-icon', 'media-qd-icon--fullscreen');
  const mainVideo = b.querySelector('.media-video-container__video');
  if (mainVideo) mainVideo.classList.add('media-video-container__video');
}
